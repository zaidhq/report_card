    private int marksInMaths;
    private int marksInHindi;

    String getName() {
        return name;
    }

    void setName(String name) {
        this.name = name;
    }

    int getMarksInEnglish() {
        return marksInEnglish;
    }

    void setMarksInEnglish(int marksInEnglish) {
        this.marksInEnglish = marksInEnglish;
    }

    int getMarksInMaths() {
        return marksInMaths;
    }

    void setMarksInMaths(int marksInMaths) {
        this.marksInMaths = marksInMaths;
    }

    int getMarksInHindi() {
        return marksInHindi;
    }

    void setMarksInHindi(int marksInHindi) {
        this.marksInHindi = marksInHindi;
    }
